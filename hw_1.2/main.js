let num = prompt("Введіть число від 0 до 9");

if (num == 0) {
  alert("=");
} else if (num == 1) {
  alert("!");
} else if (num == 2) {
  alert("@");
} else if (num == 3) {
  alert("#");
} else if (num == 4) {
  alert("$");
} else if (num == 5) {
  alert("%");
} else if (num == 6) {
  alert("^");
} else if (num == 7) {
  alert("&");
} else if (num == 8) {
  alert("*");
} else if (num == 9) {
  alert("(");
}
