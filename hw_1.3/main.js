// Запросить у пользователя трехзначное число и проверить, есть ли в нем одинаковые цифры.
let number = prompt("Введіть трьохзначне число");

if (
  +number[0] === +number[1] ||
  +number[0] === +number[2] ||
  +number[1] === +number[2]
) {
  alert("Є однакові");
} else {
  alert("Немає однакових");
}
