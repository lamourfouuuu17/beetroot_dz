// Запросить 2 числа и найти только наибольший общий делитель.
let num1 = +prompt("Введіть перше число");
let num2 = +prompt("Введіть друге число");

let d = num1 > num2 ? num2 : num1;
// 10 15
// 1  1
// 2  3
// 5  5

while (d >= 1) {
  if (num1 % d === 0 && num2 % d === 0) {
    alert(`Найбільший дільник: ${d}`);
    break;
  }
  d--;
}
